from ....utils import rest
from ....utils.api import from_api_json
from ....utils.checks import check_datetime
from ....utils.resource import Resource
from ..boletoPayment import BoletoPayment


class BoletoPaymentLog(Resource):
    """Description: BoletoPaymentLog object

    Every time a BoletoPayment entity is modified, a correspondent BoletoPaymentLog
    is generated for the entity. This log is never generated by the
    user, but it can be retrieved to check additional information
    on the BoletoPayment.

    Attributes:
        payment [BoletoPayment]: BoletoPayment entity to which the log refers to.
        id [string, default None]: unique id returned when log is created. ex: "5656565656565656"
        errors [list]: list of errors in case log was generated by webhook event
        type [string, default None]: type of the BoletoPayment event which triggered log creation. ex: "registered" or "paid"
        created [datetime.datetime, default None]: creation datetime for the payment. ex: datetime.datetime(2020, 3, 10, 10, 30, 0, 0)
    """

    def __init__(self, id, created, type, errors, payment):
        Resource.__init__(self, id=id)

        self.created = check_datetime(created)
        self.type = type
        self.errors = errors
        self.payment = from_api_json(BoletoPayment, payment)


def get(id, user=None):
    """Retrieve a single BoletoPaymentLog

    Receive a single BoletoPaymentLog object previously created by the Stark Bank API by passing its id

    Parameters (required):
        id [string]: object unique id. ex: "5656565656565656"
    Parameters (optional):
        user [Project object]: optional Project object. Not necessary if starkbank.user was set before function call
    """
    return rest.get_id(resource=BoletoPaymentLog, id=id, user=user)


def query(limit=None, payment_ids=None, events=None, user=None):
    """Retrieve BoletoPaymentLogs

    Receive a generator of BoletoPaymentLog objects previously created in the Stark Bank API

    Parameters (optional):
        limit [integer, default None]: optional number of objects to be retrieved. Unlimited if None. ex: 35
        payment_ids [list of strings, default None]: optional list of ids to filter selected objects. ex: ["5656565656565656", "4545454545454545"]
        events [string, default None]: optional filter for events of objects retrieved. ex: "paid" or "registered"
        user [Project object, default None]: optional Project object. Not necessary if starkbank.user was set before function call
    """
    return rest.get_list(resource=BoletoPaymentLog, limit=limit, user=user, events=events, payment_ids=payment_ids)
